function openEpisode(id){
  const e = byId[id] || episodes[0];
  document.getElementById('modalImage').src = e.img;
  document.getElementById('modalMeta').textContent = e.runtime + ' | ' + e.tag;
  document.getElementById('modalTitle').textContent = e.title;
  document.getElementById('modalDescription').textContent = e.description;
  document.getElementById('modalWhy').textContent = e.why;

  // Archival metadata bindings
  document.getElementById('modalCatalogId').textContent = e.catalogId;
  document.getElementById('modalCollection').textContent = e.collectionName;
  document.getElementById('modalBox').textContent = 'Box ' + e.box;
  document.getElementById('modalPeriod').textContent = e.period;

  document.getElementById('modalBackdrop').classList.add('show');
  document.getElementById('modalBackdrop').setAttribute('aria-hidden','false');
}

document.addEventListener('click', (ev)=>{
  const card = ev.target.closest('.card');
  if(card) openEpisode(card.dataset.id);
  const panelBtn = ev.target.closest('[data-panel]');
  if(panelBtn){
    const name = panelBtn.dataset.panel;
    document.querySelectorAll('.panel').forEach(p=>p.classList.remove('active-panel'));
    document.getElementById('panel-'+name).classList.add('active-panel');
    document.querySelectorAll('.dock-icon').forEach(b=>b.classList.toggle('active', b.dataset.panel===name));
    window.scrollTo({top:0,behavior:'smooth'});
  }
});

function playChoosingAnimation(targetId, callback) {
  const loader = document.getElementById('terminalLoader');
  const statusEl = document.getElementById('loaderLines');
  
  if (!loader || !statusEl) {
    callback();
    return;
  }

  statusEl.textContent = '';
  const years = ['1997', '1990', '1980', '1970'];
  const arrows = ['arrow-1', 'arrow-2', 'arrow-3'];
  
  years.forEach(yr => {
    const el = document.getElementById('year-' + yr);
    if (el) el.classList.remove('active');
  });
  arrows.forEach(arr => {
    const el = document.getElementById(arr);
    if (el) el.classList.remove('active');
  });

  loader.classList.add('show');
  loader.setAttribute('aria-hidden', 'false');

  const timelineSteps = [
    { type: 'year', id: '1997', text: '>> INITIALIZING TIME PORTAL BEACON...', delay: 100 },
    { type: 'arrow', id: 'arrow-1', text: '>> REVERSING COMPAQ DATASTREAM [➔ 1990]...', delay: 450 },
    { type: 'year', id: '1990', text: '>> CHRONO SECTOR 1990 LOCK ESTABLISHED...', delay: 800 },
    { type: 'arrow', id: 'arrow-2', text: '>> SHIFTING RETROGRADE WAVE [➔ 1980]...', delay: 1150 },
    { type: 'year', id: '1980', text: '>> CHRONO SECTOR 1980 LOCK ESTABLISHED...', delay: 1500 },
    { type: 'arrow', id: 'arrow-3', text: '>> DECRYPTING LOCAL TRAVEL NETWORK SCHEMES [➔ 1970s]...', delay: 1850 },
    { type: 'year', id: '1970', text: '>> CHRONO SECTOR 1970s LOCK ESTABLISHED. SOURCE FOUND.', delay: 2200 }
  ];

  timelineSteps.forEach(step => {
    setTimeout(() => {
      statusEl.textContent = step.text;
      if (step.type === 'year') {
        const el = document.getElementById('year-' + step.id);
        if (el) el.classList.add('active');
      } else {
        const el = document.getElementById(step.id);
        if (el) el.classList.add('active');
      }
    }, step.delay);
  });

  setTimeout(() => {
    loader.classList.remove('show');
    loader.setAttribute('aria-hidden', 'true');
    callback();
  }, 2900);
}

document.getElementById('playFeatured').addEventListener('click',()=>playChoosingAnimation('finding-each-other', ()=>openEpisode('finding-each-other')));
